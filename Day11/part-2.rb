def transpose(s)
  lines = s.split("\n")
  longest = lines.map { |line| line.length - 1 }.max

  (0..longest).map do |index|
    lines.map { |l| l[index] || ' ' }.join
  end * "\n"
end

def expand(stars)
  new_stars = ""
  stars.each_line do |line|
    unless line.include?("#")
      new_stars << "*" * (line.length - 1) + "\n"
    else
      new_stars << line
    end
  end
  new_stars
end

